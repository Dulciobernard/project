let dia = 9;

switch(dia){
	case 1:
		console.log('Domingo');
		break;
	case 2:
		console.log('Segunda');
		break;
	case 3:
		console.log('Terca-feira');
		break;
	case 4:
		console.log('Quarta-feira');
		break;
	case 5:
		console.log('Quinta-feira');
		break;
	case 6:
		console.log('Sexta-feira');
		break;
	case 7:
		console.log('Sabado');
		break;
	default:
		console.log('A semana so tem 7 dias');

}
