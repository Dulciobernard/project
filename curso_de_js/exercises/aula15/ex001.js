/*
 3. Métodos Básicos de Arrays
Use o método push para adicionar "manga" ao final do array.
Use o método pop para remover o último item do array.
Use o método shift para remover o primeiro item do array.
Use o método unshift para adicionar "morango" ao início do array.
*/
let object = ['balde', 'janela', 'laraja', 'roupa', 'mouse'];

object.push('manga');
console.log(object);
object.pop();
object.shift();
object.unshift('morango');

console.log(object);
